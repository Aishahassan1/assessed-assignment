const processInput = () => {
  const submit = document.querySelector("#submit");
  submit.addEventListener("click", (event) => {
    event.preventDefault();
    const confirmationMessage = document.querySelector("#submit-confirmation");
    const name = document.querySelector("#name");
    const password = document.querySelector("#password");
    const email = document.querySelector("#email");

    confirmationMessage.textContent = `${name.value} Thank you for registering with Tasty TV! We will email you soon on: ${email.value} with all the latest TV and movie news!`;
    if (name.value === "") {
      alert("Name is required");
      confirmationMessage.style.display = "none";
    }
    if (password.value.length < 6) {
      alert("password must be longer than 6 characters");
      confirmationMessage.style.display = "none";
    }
    if (email.value.length < 5) {
      alert("Email is invalid");
      confirmationMessage.style.display = "none";
    } else {
      confirmationMessage.style.display = "display";
    }
  });
};
